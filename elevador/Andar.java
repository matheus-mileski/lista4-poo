package lista4ex1;

public class Andar {

	private int numeroAndar;
	private int capacidade = 1;

	public Andar() {

	}

	public void setNumeroAndar(int n) {
		numeroAndar = n;
	}

	public int getNumeroAndar() {
		return numeroAndar;
	}

	public void setCapacidade(int n) {
		capacidade = n;
	}

	public int getCapacidade() {
		return capacidade;
	}

}
